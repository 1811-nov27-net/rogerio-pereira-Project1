using Microsoft.EntityFrameworkCore;
using Project0.DataAccess;
using Project0.DataAccess.Repositories;
using System;
using System.Collections.Generic;
using System.Text;

namespace Project0.Library.Control.Model
{
    public class PizzaController
    {
        PizzaRepository repository = null;

        public PizzaController()
        {
            repository = new PizzaRepository(DbOptions.Context);
        }

        public List<Pizzas> getAll()
        {
            return (List<Pizzas>)repository.GetAll();
        }

        public List<Pizzas> GetAllWithIngredients()
        {
            return (List<Pizzas>)repository.GetAllWithIngredients();
        }

        public Pizzas FindById(int id)
        {
            return (Pizzas)repository.GetById(id);
        }

        public List<Pizzas> FindByName(string name)
        {
            return (List<Pizzas>)repository.GetByName(name);
        }

        public Pizzas Save(Pizzas Pizza)
        {
            Pizzas c = (Pizzas)repository.Save(Pizza);
            repository.SaveChanges();
            return c;
        }

        public void Delete(int id)
        {
            repository.Delete(id);
            repository.SaveChanges();
        }

        public Pizzas Update(Pizzas Pizza)
        {
            repository.Save(Pizza, Pizza.Id);
            repository.SaveChanges();
            return Pizza;
        }

        public bool CheckStock(Pizzas pizza)
        {
            return repository.CheckStock(pizza);
        }

        public void DecreaseStock(Pizzas pizza)
        {
            repository.DecreaseStock(pizza);
        }
    }
}
